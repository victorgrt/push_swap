#include "libft.h"

int	ft_isalnum(int c)
{
	unsigned char	c2;

	c2 = c;
	if ((c2 >= '0' && c2 <= '9') || ((c2 >= 'A' && c2 <= 'Z')
			|| (c2 >= 'a' && c2 <= 'z')))
		return (1);
	return (0);
}

